//----------------------------------------------------------------------------
// 名    称：   uint16 GetCRC16(uint8 *Buf, uint16 Len)
// 功    能：   取CRC16校验和
// 入口参数：  
// 出口参数：   
//----------------------------------------------------------------------------
uint16 GetCrc16Check(uint8 *Buf, uint16 Len)
{
    uint16 crc = 0x0;
    uint16 i, tmp1;										
    
    for (i=0; i< Len; i++) {
        tmp1 = crc;
        crc = sd_crc16_table[(tmp1>>8) ^ *Buf++];
        crc = crc ^ (tmp1<<8);
    }
    
    return(crc);
}

