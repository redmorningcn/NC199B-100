uint16_t crc16(uint8_t *ptr, uint8_t len)  
{
	uint32_t i;
	uint16_t crc16=0xFFFF;
	for(i=0;i<len;i++)
	{
		crc16=( crc16 >> 8 ) ^ crc16tab[ ( crc16 & 0xFF ) ^ ptr[i]]; 
	}
	return crc16;
}
